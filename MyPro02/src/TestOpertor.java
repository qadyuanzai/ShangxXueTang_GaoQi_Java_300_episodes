/**
 * 位运算符
 * @author 张思源
 *
 */
public class TestOpertor {
	public static void main(String[] args) {
		int a = 3;
		int b = 4;
		
		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(a^b);
		System.out.println(~a);
		
		System.out.println("~~~~~~~~~~~~");
		System.out.println(b<<2);
		System.out.println(b>>1);
		System.out.println(a>>1);
		
	}
}
